def foo(a,b=1,*c,**d):
    print(f'a={a}\nb={b}\nc={c}\nd={d}')

print(foo(1))
print('---------------------------------------------------------------------------------------------')
print(foo(1, 2))
print('---------------------------------------------------------------------------------------------')
print(foo(1, 2, 3))
print('---------------------------------------------------------------------------------------------')
print(foo(1, 2, (3, 4, 5)))
print('---------------------------------------------------------------------------------------------')
print(foo(1, 2, *(3, 4, 5))) #拆箱参数，*拆元组
print('---------------------------------------------------------------------------------------------')
print(foo(1, 2, 3, x=4))
print('---------------------------------------------------------------------------------------------')
print(foo(1, 2, *(3, 4), **{'x': 5, 'y': 6})) #拆箱参数，*拆词典
